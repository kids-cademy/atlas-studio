package com.kidscademy.atlas.studio.tool;

public enum InstrumentCategory {
    // ENUM('PERCUSSION','STRINGS','WOODWIND','KEYBOARD')
    /** Instrument is sounded by being struck or scraped by a beater. */
    PERCUSSION,
    /**
     * Produce sound from vibrating strings transmitted to the body of the
     * instrument.
     */
    STRINGS,
    /**
     * Produce sound by directing a focused stream of air across the edge of a hole
     * in a cylindrical tube.
     */
    WOODWIND,
    /** A keyboard instrument is a musical instrument played using a keyboard. */
    KEYBOARD
}